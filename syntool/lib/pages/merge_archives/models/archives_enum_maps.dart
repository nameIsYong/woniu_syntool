/// 位运算枚举值转中文名称
/// 例如 value=532, map={1:'无',2:'高血压',4:'糖尿病'} -> '高血压,糖尿病,慢性阻塞性肺疾病,肝炎'
String bitEnumToString(int? value, Map<int, String> enumMap) {
  if (value == null || value == 0) return '';
  final List<String> results = [];
  enumMap.forEach((key, label) {
    if (key != 0 && (value & key) == key) {
      results.add(label);
    }
  });
  return results.join(', ');
}

/// 通用枚举值转换
String enumToString(dynamic value, Map<dynamic, String> enumMap) {
  if (value == null) return '';
  return enumMap[value] ?? value.toString();
}
